    private void approveButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_approveButtonActionPerformed
    {//GEN-HEADEREND:event_approveButtonActionPerformed

        /* Get credentials */
        String user = usernameTextField.getText();
        char[] charPass = passPasswordField.getPassword();
        String pass = String.copyValueOf(charPass);
        Utils.UserType userType;

        /* Check credentials */
        if (userInstance.ValidateUserName(user) != Utils.ValidateUser.VALIDATION_OK) {
            if (esToggleButton.isSelected()) {
                JOptionPane.showMessageDialog(null, "Nombre de usuario desconocido.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Unknown user name.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            return;
        }

        if (userInstance.ValidateUserPassword(user, pass) == Utils.ValidateUser.WRONG_PASSWORD) {
            if (esToggleButton.isSelected()) {
                JOptionPane.showMessageDialog(null, "Contrase침a incorrecta para el nombre de usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Wrong password for the user name.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            return;
        }

        /* Credentials are OK -> Set usertype */
        userType = userInstance.GetUserType(user);
        if (userType.equals(Utils.UserType.UNKNOWN_TYPE)) {
            if (esToggleButton.isSelected()) {
                JOptionPane.showMessageDialog(null, "Tipo de usuario desconocido para este nombre de usuario.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(null, "Unknown user type for this user name.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            return;
        }

        this.dispose();

        if (esToggleButton.isSelected()) {
            Planning_ES window = new Planning_ES(userType);
            window.setExtendedState(JFrame.MAXIMIZED_BOTH);
            window.setVisible(true);
//            window.setResizable(false);
        }
        else {
            Planning_EN window = new Planning_EN(userType);
            window.setExtendedState(JFrame.MAXIMIZED_BOTH);
            window.setVisible(true);
//            window.setResizable(false);
        }
    }//GEN-LAST:event_approveButtonActionPerformed

